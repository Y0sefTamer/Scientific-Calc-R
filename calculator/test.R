library(testthat)
source("functions.R") # لازم تربط ملفك عشان يشوف الدوال

# ده الاختبار
test_that("اختبار عمليات الجمع", {
  
  # 1. بنادي الدالة بتاعتك وبخزن نتيجتها في متغير
  result <- add(5, 5)
  
  # 2. بقول للمكتبة: "توقعي إن النتيجة تكون 10"
  expect_equal(result, 10)
  
  # 3. نجرب حالة تانية (أرقام سالبة)
  expect_equal(add(-1, 1), 0)
})

